export const getDailyQuote = (date = new Date()): DailyQuote => {
  const localDayStart = new Date(date.getFullYear(), date.getMonth(), date.getDate()).getTime();
  const daysFromEpoch = Math.floor(localDayStart / 86_400_000);
  const halfDaySlot = date.getHours() >= 12 ? 1 : 0;
  const index = Math.abs(daysFromEpoch * 2 + halfDaySlot) % DAILY_QUOTES.length;
  return DAILY_QUOTES[index];
};
